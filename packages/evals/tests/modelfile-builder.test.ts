import { assert, describe, it } from "vite-plus/test";
import {
  buildModelfile,
  ModelfileBuilderError,
  MODELFILE_MESSAGE_ROLES,
  type MessageEntry,
} from "../src/distill/modelfile-builder";
import { convertTrainingMessagesToModelfileMessages } from "../src/distill/modelfile-messages";
import { TrainingMessage, TrainingToolCall } from "../src/distill/types";

describe("buildModelfile", () => {
  it("produces FROM, PARAMETER, SYSTEM, and ADAPTER directives in order", () => {
    const output = buildModelfile({
      baseModel: "gemma4:e4b",
      adapterPath: "./adapters/gemma4-perfagent.gguf",
      systemPrompt: "You are a test agent.",
      parameters: [
        { name: "temperature", value: 0.1 },
        { name: "num_ctx", value: 32768 },
      ],
    });
    const fromIdx = output.indexOf("FROM gemma4:e4b");
    const adapterIdx = output.indexOf("ADAPTER ./adapters/gemma4-perfagent.gguf");
    const paramIdx = output.indexOf("PARAMETER temperature 0.1");
    const systemIdx = output.indexOf("SYSTEM ");
    assert.isAbove(fromIdx, -1);
    assert.isAbove(adapterIdx, fromIdx);
    assert.isAbove(paramIdx, adapterIdx);
    assert.isAbove(systemIdx, paramIdx);
    assert.include(output, "PARAMETER num_ctx 32768");
  });

  it("wraps multi-line SYSTEM in triple-quote heredoc", () => {
    const output = buildModelfile({
      baseModel: "gemma4:e4b",
      systemPrompt: "Line one.\nLine two.\nLine three.",
    });
    assert.include(output, 'SYSTEM """');
    assert.include(output, "Line one.");
    assert.include(output, "Line three.");
    assert.match(output, /"""\s*$/m);
  });

  it("throws ModelfileBuilderError on empty baseModel", () => {
    let thrown: unknown;
    try {
      buildModelfile({ baseModel: "" });
    } catch (error) {
      thrown = error;
    }
    assert.instanceOf(thrown, ModelfileBuilderError);
  });

  it("throws on PARAMETER name containing whitespace", () => {
    let thrown: unknown;
    try {
      buildModelfile({
        baseModel: "gemma4:e4b",
        parameters: [{ name: "bad name", value: 1 }],
      });
    } catch (error) {
      thrown = error;
    }
    assert.instanceOf(thrown, ModelfileBuilderError);
  });

  it("emits MESSAGE directives with role tags for few-shot examples", () => {
    const output = buildModelfile({
      baseModel: "gemma4:e4b",
      exampleMessages: [
        { role: "user", content: "hello" },
        { role: "assistant", content: "hi" },
      ],
    });
    assert.include(output, "MESSAGE user hello");
    assert.include(output, "MESSAGE assistant hi");
  });

  it("prefixes header comments with '# '", () => {
    const output = buildModelfile({
      baseModel: "gemma4:e4b",
      header: "Generated by test\nLine two",
    });
    assert.include(output, "# Generated by test\n");
    assert.include(output, "# Line two\n");
  });

  it("rejects MESSAGE role 'tool' (Round 1 review C2)", () => {
    // Ollama Modelfile grammar accepts only system|user|assistant for MESSAGE.
    // The type system also forbids this at compile time; the runtime check
    // belts-and-braces it for callers that type-cast.
    assert.notInclude(MODELFILE_MESSAGE_ROLES as ReadonlyArray<string>, "tool");
    let thrown: unknown;
    try {
      buildModelfile({
        baseModel: "gemma4:e4b",
        exampleMessages: [{ role: "tool" as MessageEntry["role"], content: "nope" }],
      });
    } catch (error) {
      thrown = error;
    }
    assert.instanceOf(thrown, ModelfileBuilderError);
  });
});

describe("convertTrainingMessagesToModelfileMessages", () => {
  it("drops role='system' messages (M4) — SYSTEM directive already owns the slot", () => {
    const converted = convertTrainingMessagesToModelfileMessages([
      new TrainingMessage({ role: "system", content: "big prompt" }),
      new TrainingMessage({ role: "user", content: "hello" }),
      new TrainingMessage({ role: "assistant", content: "hi" }),
    ]);
    assert.strictEqual(converted.length, 2);
    assert.strictEqual(converted[0].role, "user");
    assert.strictEqual(converted[1].role, "assistant");
    for (const entry of converted) {
      assert.notStrictEqual(entry.role, "system");
    }
  });

  it("inlines role='tool' messages into the preceding assistant turn (C2)", () => {
    const converted = convertTrainingMessagesToModelfileMessages([
      new TrainingMessage({ role: "user", content: "go" }),
      new TrainingMessage({
        role: "assistant",
        content: "calling",
        toolCalls: [new TrainingToolCall({ id: "tc-0", name: "observe", arguments: "{}" })],
      }),
      new TrainingMessage({
        role: "tool",
        content: "observed: ok",
        toolCallId: "tc-0",
      }),
      new TrainingMessage({ role: "assistant", content: "done" }),
    ]);
    for (const entry of converted) {
      assert.notStrictEqual(entry.role, "tool" as unknown);
    }
    const assistantTurns = converted.filter((entry) => entry.role === "assistant");
    assert.strictEqual(assistantTurns.length, 2);
    assert.include(assistantTurns[0].content, "<tool_calls>");
    assert.include(assistantTurns[0].content, '<tool_result id="tc-0">');
    assert.include(assistantTurns[0].content, "observed: ok");
    assert.strictEqual(assistantTurns[1].content, "done");
  });

  it("appends <tool_calls> block when assistant has toolCalls", () => {
    const converted = convertTrainingMessagesToModelfileMessages([
      new TrainingMessage({
        role: "assistant",
        content: "going",
        toolCalls: [new TrainingToolCall({ id: "x", name: "click", arguments: '{"n":1}' })],
      }),
    ]);
    assert.strictEqual(converted.length, 1);
    assert.match(converted[0].content, /<tool_calls>.*click.*<\/tool_calls>/);
  });

  it("produces a Modelfile with exactly one SYSTEM and zero MESSAGE system (M4)", () => {
    const converted = convertTrainingMessagesToModelfileMessages([
      new TrainingMessage({ role: "system", content: "prompt" }),
      new TrainingMessage({ role: "user", content: "hi" }),
    ]);
    const output = buildModelfile({
      baseModel: "gemma4:e4b",
      systemPrompt: "prompt",
      exampleMessages: converted,
    });
    const systemDirectiveCount = output.match(/^SYSTEM /gm)?.length ?? 0;
    const messageSystemCount = output.match(/^MESSAGE system /gm)?.length ?? 0;
    assert.strictEqual(systemDirectiveCount, 1);
    assert.strictEqual(messageSystemCount, 0);
  });
});
