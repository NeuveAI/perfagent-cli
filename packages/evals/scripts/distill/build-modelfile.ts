import * as fs from "node:fs";
import * as path from "node:path";
import { Config, Effect, Schema } from "effect";
import { buildLocalAgentSystemPrompt } from "@neuve/shared/prompts";
import {
  buildModelfile,
  ModelfileBuilderError,
  type MessageEntry,
} from "../../src/distill/modelfile-builder";
import { convertTrainingMessagesToModelfileMessages } from "../../src/distill/modelfile-messages";
import { TrainingSample } from "../../src/distill/types";

const DEFAULT_BASE_MODEL = "gemma4:e4b";
const DEFAULT_INPUT = "packages/evals/data/distill/out/teacher-data.jsonl";
const DEFAULT_OUTPUT = "packages/evals/data/distill/out/Modelfile";
const DEFAULT_ADAPTER = "./adapters/gemma4-perfagent.gguf";

const decodeSample = Schema.decodeUnknownSync(TrainingSample);
const JsonLine = Schema.fromJsonString(Schema.Unknown);
const decodeJsonLine = Schema.decodeUnknownSync(JsonLine);

const readExampleMessages = (jsonlPath: string, limit: number): ReadonlyArray<MessageEntry> => {
  if (!fs.existsSync(jsonlPath)) return [];
  const contents = fs.readFileSync(jsonlPath, "utf8");
  const lines = contents.split("\n").filter((line) => line.length > 0);
  if (lines.length === 0) return [];
  const messages: MessageEntry[] = [];
  for (const line of lines.slice(0, limit)) {
    const sample = decodeSample(decodeJsonLine(line));
    messages.push(...convertTrainingMessagesToModelfileMessages(sample.messages));
  }
  return messages;
};

const baseModelConfig = Config.string("EVAL_DISTILL_BASE_MODEL").pipe(
  Config.withDefault(DEFAULT_BASE_MODEL),
);
const inputConfig = Config.string("EVAL_DISTILL_INPUT").pipe(Config.withDefault(DEFAULT_INPUT));
const outputConfig = Config.string("EVAL_DISTILL_MODELFILE").pipe(
  Config.withDefault(DEFAULT_OUTPUT),
);
const adapterConfig = Config.string("EVAL_DISTILL_ADAPTER").pipe(
  Config.withDefault(DEFAULT_ADAPTER),
);
const includeExamplesConfig = Config.boolean("EVAL_DISTILL_INCLUDE_EXAMPLES").pipe(
  Config.withDefault(true),
);

const program = Effect.gen(function* () {
  const baseModel = yield* baseModelConfig;
  const input = yield* inputConfig;
  const output = yield* outputConfig;
  const adapterPath = yield* adapterConfig;
  const includeExamples = yield* includeExamplesConfig;

  const exampleMessages = includeExamples ? readExampleMessages(input, 1) : [];

  const modelfile = yield* Effect.try({
    try: () =>
      buildModelfile({
        baseModel,
        adapterPath,
        systemPrompt: buildLocalAgentSystemPrompt(),
        parameters: [
          { name: "temperature", value: 0.1 },
          { name: "num_ctx", value: 32768 },
        ],
        exampleMessages,
        header: [
          "Modelfile generated by packages/evals/scripts/distill/build-modelfile.ts",
          "Base model: " + baseModel,
          "Adapter: " + adapterPath,
          "Source JSONL: " + input,
        ].join("\n"),
      }),
    catch: (cause) =>
      cause instanceof ModelfileBuilderError
        ? cause
        : new ModelfileBuilderError({
            reason: cause instanceof Error ? cause.message : String(cause),
          }),
  });

  fs.mkdirSync(path.dirname(output), { recursive: true });
  fs.writeFileSync(output, modelfile, "utf8");

  yield* Effect.logInfo("Modelfile written", { output, byteCount: modelfile.length });
  console.log(
    JSON.stringify(
      { output, byteCount: modelfile.length, baseModel, adapterPath, sourceJsonl: input },
      null,
      2,
    ),
  );
});

await Effect.runPromise(program);
